import fs from 'fs';
import path from 'path';
import { globSync } from 'glob';

const projectRoot = process.cwd();
const pugFiles = globSync('src/**/*.pug', { cwd: projectRoot });

let allContent = '';
pugFiles.forEach(file => {
  const content = fs.readFileSync(path.resolve(projectRoot, file), 'utf8');
  allContent += content + ' ';
});

// Extract everything that looks like a class or possible tailwind utility
// This is a broad regex to be safe
const classes = allContent.match(/class=['"]([^'"]+)['"]/g) || [];
const uniqueClasses = [...new Set(classes.map(c => c.replace(/class=['"]|['"]/g, '')))];

const outputPath = path.resolve(projectRoot, 'src/styles/tailwind-scan.js');
const outputContent = `// Auto-generated by generate-tailwind-scan.js
// This file ensures Tailwind scans all classes used in Pug templates.
// TAILWIND_SCAN_CLASSES: ${uniqueClasses.join(' ')}
export const tailwindClasses = "${uniqueClasses.join(' ')}";
`;


fs.writeFileSync(outputPath, outputContent);
console.log(`Generated ${outputPath} with ${uniqueClasses.length} class entries.`);
